#include "../includes/ft_header.h"

const char	*ft_strrchr(const char *str, char c)
{
	int	i;

	i = 0;
	while (*(str + i))
		i++;
	while (i)
	{
		if (*(str + i - 1) == c)
			return (str + i - 1);
		i--;
	}
	return (NULL);
}
