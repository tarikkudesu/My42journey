#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	i = 0;
	if (ac > 0)
	{
		while (*(*av + i))
		{
			write (1, &*(*av + i), 1);
			i++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
