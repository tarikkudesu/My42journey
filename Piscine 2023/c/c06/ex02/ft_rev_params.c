#include <unistd.h>

int	main(int ac, char **av)
{
	int	s;
	int	c;

	s = ac - 1;
	if (ac > 1)
	{
		while (s > 0)
		{
			c = 0;
			while (*(*(av + s) + c))
			{
				write (1, &*(*(av + s) + c), 1);
				c++;
			}
			write (1, "\n", 1);
			s--;
		}
	}
	return (0);
}
