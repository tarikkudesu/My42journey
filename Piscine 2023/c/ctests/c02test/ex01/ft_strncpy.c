#include <unistd.h>
#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int main()
{
	char src[] = "shittyday";
	char dest[] = "dfghkjhgfs";
	int n;

	ft_strncpy(dest, src, 3);
	printf("%s", dest);
}
